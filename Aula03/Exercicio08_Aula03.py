# ESCREVA UM PROGRAMA EM PYTHON QUE RECEBE UM INTEIRO E DIGA SE É PAR OU ÍMPAR

numero = int(input('Insira um número para descobrir se ele é par ou impar: '))

if numero % 2 == 0:
    print(f'O número {numero} é par.')
else:
    print(f'Seu número {numero} é impar')
    
