idade = int(input('Quantos anos você tem: '))

if idade >= 18:
    print(f'Parabéns você é maior de 18 anos, já pode beber e dirigir!')
elif idade < 18:
    print(f'Vixe, você é menor de idade ainda não pode beber e dirigir!')
    

    
