# FAÇA UM ALGORITMO PARA LER DOIS NÚMEROS INTEIROS E INFORMAR SE ESTES SÃO IGUAIS OU DIFERENTES

print('Insira dois números inteiros e iremos informar se são \ndiferentes ou não!')
num1 = int(input('Escreva o primeiro número: '))
num2 = int(input('Escreva o segundo número: '))

if num1 == num2:
    print('Os números são idênticos!')
else:
    print('Os números são diferentes!')
